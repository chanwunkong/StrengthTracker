window.saveUserData = async function () {
    const user = auth.currentUser;
    if (!user) {
        alert('Ë´ãÂÖàÁôªÂÖ•ÔºÅ');
        return;
    }

    // ÂèñÂæóËº∏ÂÖ•ÂÄº
    const recordDate = document.getElementById('recordDate').value;
    if (!recordDate) {
        alert('Ë´ãÈÅ∏ÊìáÁ¥ÄÈåÑÊó•Êúü');
        return;
    }

    const gender = document.getElementById('gender').value;
    const height = parseFloat(document.getElementById('height').value);
    const weight = parseFloat(document.getElementById('weight').value);
    const bodyFat = parseFloat(document.getElementById('bodyFat').value);
    const wrist = parseFloat(document.getElementById('wrist').value);
    const ankle = parseFloat(document.getElementById('ankle').value);

    const userDocRef = doc(db, 'users', user.uid);

    const userData = {
        gender,
        height,
        wrist,
        ankle,
        records: {
            [recordDate]: {
                weight,
                bodyFat
            }
        }
    };

    try {
        const existingDoc = await getDoc(userDocRef);
        if (existingDoc.exists()) {
            // Âêà‰ΩµÊñ∞Á¥ÄÈåÑÂà∞ÁèæÊúâÁ¥ÄÈåÑ
            const oldData = existingDoc.data();
            const updatedRecords = { ...(oldData.records || {}), ...userData.records };

            await setDoc(userDocRef, {
                ...oldData,
                gender,
                height,
                wrist,
                ankle,
                records: updatedRecords
            });

        } else {
            await setDoc(userDocRef, userData);
        }

        alert('Ë≥áÊñôÂ∑≤ÊàêÂäüÂÑ≤Â≠òÔºÅ');
    } catch (error) {
        console.error('ÂÑ≤Â≠òÂ§±ÊïóÔºö', error);
        alert('Ë≥áÊñôÂÑ≤Â≠òÂ§±ÊïóÔºåË´ãÁ®çÂæåÂÜçË©¶„ÄÇ');
    }
}



